<template>
    <li :class="classes">{{ data.label }}<i v-if="data.children && data.children.length" class="fez-icon fez-icon-arrow-right-b"></i></li>
</template>
<script>
export default {
    name: 'Casitem',
    props: {
        data: Object,
        prefixCls: String,
        tmpItem: Object
    },
    computed: {
        classes() {
            return [
                `${this.prefixCls}-menu-item`, {
                    [`${this.prefixCls}-menu-item-active`]: this.tmpItem.value === this.data.value,
                    [`${this.prefixCls}-menu-item-disabled`]: this.data.disabled
                }
            ];
        }
    }
};
</script>
